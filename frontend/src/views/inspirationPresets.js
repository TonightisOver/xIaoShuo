/**
 * 按 category 获取 suggestion（未知 category 回退到 general）。
 * 返回深拷贝，避免调用方修改污染预设数据。
 */
export function getSuggestion(category) {
  const sel = SUGGESTION_DICT[category] || SUGGESTION_DICT.general
  return {
    titles: [...sel.titles],
    descriptions: [...sel.descriptions],
    defaultThemes: [...sel.defaultThemes],
    type: sel.type,
  }
}

